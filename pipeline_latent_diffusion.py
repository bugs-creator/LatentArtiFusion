    def blend_images(self,image1, image2, mask):
        # 将PIL图像转换为numpy数组
        image1_np = np.array(image1)
        image2_np = np.array(image2)
        mask_np = np.array(mask)

        if len(mask_np.shape) == 2 or mask_np.shape[2] == 1:
            mask_np = np.stack([mask_np] * 3, axis=-1)

        # 使用mask拼接图像
        blended_image_np = np.where(mask_np, image1_np, image2_np)

        # 将numpy数组转换回PIL图像
        blended_image = Image.fromarray(blended_image_np)

        return blended_image

    def adjust_mean_and_variance(self,image1,image2, mask):
        # 将PIL图像转换为numpy数组
        image1_np = np.array(image1)
        image2_np=np.array(image2)
        mask_np = np.array(mask)

        # 使用mask选择图像中的元素
        selected_elements = image1_np[mask_np == 255]
        selected2_elements= image2_np[mask_np == 255]

        # 计算选定元素的均值和方差
        target_mean = np.mean(selected_elements)
        target_variance = np.var(selected_elements)

        current_mean= np.mean(selected2_elements)
        current_variance=np.var(selected2_elements)

        # 调整图像的均值和方差
        adjusted_image_np = (image2_np - current_mean) * (target_variance / current_variance) ** 0.5 + target_mean
        adjusted_image_np = np.clip(adjusted_image_np, 0, 255)
        # 将numpy数组转换回PIL图像
        adjusted_image = Image.fromarray(adjusted_image_np.astype(np.uint8))

        return adjusted_image

