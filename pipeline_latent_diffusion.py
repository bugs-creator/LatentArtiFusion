        # 4. Prepare timesteps
        self.scheduler.set_timesteps(num_inference_steps, device=device)
        timesteps = self.scheduler.timesteps

        # 5. Prepare latent variables
        num_channels_latents = self.unet.config.in_channels

        def preprocess(image):
            augmentations = transforms.Compose(
                [
                    transforms.Resize(256, interpolation=transforms.InterpolationMode.BILINEAR),
                    transforms.CenterCrop(256) if True else transforms.RandomCrop(256),
                    transforms.ToTensor(),
                    transforms.Normalize([0.5], [0.5]),
                ]
            )

            def transform_images(examples):
                images = [augmentations(image.convert("RGB")) for image in examples]
                return {"input": images}

            images = transform_images([image])
            images = images["input"][0].unsqueeze(0)
            return images

        if original_image is None:
            latents = self.prepare_latents(
                batch_size,
                num_channels_latents,
                height,
                width,
                device,
                generator,
                latents,
            )
        else:
            _original_image=original_image
            original_image=preprocess(original_image).to(self.device)
            latents = self.vae.encode(original_image).latent_dist.sample()
            latents = latents * self.vae.config.scaling_factor

            original_latents=latents.clone()

            noisy_latents=   self.prepare_latents(
                batch_size,
                num_channels_latents,
                height,
                width,
                device,
                generator,
                latents,
            )

        if mask_image is not None:
            # mask_image=mask_image.resize((latents.shape[2], latents.shape[3]), resample=Image.NEAREST)
            _mask_image=mask_image
            mask_image=preprocess(mask_image).to(self.device)
            mask_latents=torch.nn.functional.interpolate(mask_image, scale_factor=0.125, mode='nearest')
            mask_latents = (torch.stack([mask_latents[0][0],mask_latents[0][0],mask_latents[0][0],mask_latents[0][0]]).unsqueeze(0)+1)/2

            latents=(1-mask_latents)*noisy_latents+mask_latents*latents

        # 6. Prepare extra step kwargs. TODO: Logic should ideally just be moved out of the pipeline
        extra_step_kwargs = self.prepare_extra_step_kwargs(generator, eta)

        mean=original_latents.mean()
        var=original_latents.var()

        # 7. Denoising loop
        num_warmup_steps = len(timesteps) - num_inference_steps * self.scheduler.order
        self._num_timesteps = len(timesteps)
        with self.progress_bar(total=num_inference_steps) as progress_bar:
            for i, t in enumerate(timesteps):
                # expand the latents if we are doing classifier free guidance
                latent_model_input = latents
                latent_model_input = self.scheduler.scale_model_input(latent_model_input, t)

                # predict the noise residual
                noise_pred = self.unet(
                    latent_model_input,
                    t,
                    return_dict=False,
                )[0]


                # compute the previous noisy sample x_t -> x_t-1
                latents = self.scheduler.step(noise_pred, t, latents, **extra_step_kwargs, return_dict=False)[0]
                if mask_image is not None:
                    if len(timesteps)-i<free_generate_steps:
                        pass
                    else:
                        latents = mask_latents*original_latents+(1-mask_latents)*latents
                    current_mean = torch.mean(latents)
                    current_variance = torch.var(latents)

                    # latents = (latents - current_mean) * (
                    #             var / current_variance) ** 0.5 + mean

                if callback_on_step_end is not None:
                    callback_kwargs = {}
                    for k in callback_on_step_end_tensor_inputs:
                        callback_kwargs[k] = locals()[k]
                    callback_outputs = callback_on_step_end(self, i, t, callback_kwargs)

                    latents = callback_outputs.pop("latents", latents)

                # call the callback, if provided
                if i == len(timesteps) - 1 or ((i + 1) > num_warmup_steps and (i + 1) % self.scheduler.order == 0):
                    progress_bar.update()
                    if callback is not None and i % callback_steps == 0:
                        step_idx = i // getattr(self.scheduler, "order", 1)
                        callback(step_idx, t, latents)

        if not output_type == "latent":
            latents=latents.to(self.vae.dtype)
            image = self.vae.decode(latents / self.vae.config.scaling_factor, return_dict=False, generator=generator)[
                0
            ]

            # image=original_image*mask_image+(1-mask_image)*image
        else:
            image = latents
            has_nsfw_concept = None

        # if has_nsfw_concept is None:
        #     do_denormalize = [True] * image.shape[0]
        # else:
        #     do_denormalize = [not has_nsfw for has_nsfw in has_nsfw_concept]
        do_denormalize = [True] * image.shape[0]

        image = self.image_processor.postprocess(image, output_type=output_type, do_denormalize=do_denormalize)[0]

        image=self.adjust_mean_and_variance(image1=_original_image,image2=image,mask=_mask_image)
        # image=self.blend_images(image1=_original_image,image2=image,mask=_mask_image)

        # Offload all models
        self.maybe_free_model_hooks()

        if not return_dict:
            return (image, has_nsfw_concept)

        return StableDiffusionPipelineOutput(images=image, nsfw_content_detected=None)